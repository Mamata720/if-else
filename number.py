num=int(input("enter first number"))
num1=int(input("enter second number"))
op=input("enter mathematical operator")
if op=='+':
    print("result is",num+num1)
if op=='-':
    print("result is",num-num1)
if op=='*':
    print("result is",num*num1)
if op=='/':
    print("result is",num/num1)
if op=='%':
    print("result is",num%num1)
if op=='**':
    print("result is",num**num1)
if op=='//':
    print("result is",num//num1)